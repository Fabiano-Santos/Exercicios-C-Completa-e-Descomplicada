#include<stdio.h>
#include<stdlib.h>

int main(){

    int horas, segundos, minutos, resto;

    printf("----------------------------------------------------------------------------------------------\n");
    printf("<<<<<<<< Este programa lê um número inteiro de 4 digitos e imprime 1 digito por linha >>>>>>>>\n");
    printf("----------------------------------------------------------------------------------------------\n");
         
    printf("Digite um número inteiro em segundos: ");
    scanf("%i", &segundos);

    horas = segundos /3600; 
    resto = segundos % 3600;
    minutos = resto/60;
    segundos = minutos % 60;
    
    printf("%i horas %i minutos e %i segundos", horas, minutos, segundos);

    printf("\n--------------------------------------------------------------------------------------------\n");
    printf("<<<<<< FIM DO PROGRAMA!!! >>>>>>");
    printf("\n--------------------------------------------------------------------------------------------\n");

  return 0;
}
